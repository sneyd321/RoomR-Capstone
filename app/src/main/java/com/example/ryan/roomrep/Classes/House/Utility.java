package com.example.ryan.roomrep.Classes.House;

public class Utility {

    private String name;
    private String responsibilityOf;


    public Utility(String name, String responsibilityOf) {
        this.name = name;
        this.responsibilityOf = responsibilityOf;
    }

    public String getResponsibilityOf() {
        return responsibilityOf;
    }

    public String getName() {
        return name;
    }

}


