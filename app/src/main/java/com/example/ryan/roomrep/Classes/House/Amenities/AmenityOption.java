package com.example.ryan.roomrep.Classes.House.Amenities;

public class AmenityOption {

    String name;
    boolean checked;

    public AmenityOption(boolean value, String name) {
        this.name = name;
        this.checked = value;
    }

    public String getName(){
        return this.getName();
    }

    public boolean isChecked() {
        return this.checked;
    }

}
